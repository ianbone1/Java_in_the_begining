public class Calculator {


    public Calculator(){
    }

    public int add(int lhs, int rhs){
        return lhs + rhs;
    }

    public int subtract(int lhs, int rhs){
        return lhs - rhs;
    }

    public int multiply(int lhs, int rhs){
        return lhs * rhs;
    }

    public double divide(double lhs, double rhs){
        return (lhs / rhs);
    }
}
